//Converts distance needed to travel from centimeters to the counts from the encoders
long calculate (long distancetraveled){
	long radius = (2.75/2)*2.54;
	long circumference = radius*2*PI;
	long encoderCounts = (360/circumference)*distancetraveled;
	return encoderCounts;
}

//Moving Forward

void forwardstraight(long encoderCounts, bool lift){

	if(lift == false)
		SensorValue[TR] = 0;
	SensorValue[TL] = 0;

	if(SensorValue[TR]*cos(45)*2.54 <= encoderCounts && abs(SensorValue[TL])*cos(45)*2.54 <= encoderCounts
		){
		motor[topRight] = FULL_POWER;
		motor[topLeft] =  FULL_POWER;
		motor[bottomRight]= FULL_POWER;
		motor[bottomLeft] = FULL_POWER;

	}
	else{
		motor[topRight] = 0;
		motor[topLeft] =  0;
		motor[bottomRight]= 0;
		motor[bottomLeft] = 0;
	}


}

//Moving Backwards

void backwardstraight(long encoderCounts, bool lift2){

	if(lift2 == false){
		SensorValue[TR] = 0;
		SensorValue[TL] = 0;
	}

	if(SensorValue[TR]*cos(45)*2.54 <= encoderCounts && abs(SensorValue[TL])*cos(45)*2.54 <= encoderCounts
		){
		motor[topRight] = -FULL_POWER;
		motor[topLeft] =  -FULL_POWER;
		motor[bottomRight]= -FULL_POWER;
		motor[bottomLeft] = -FULL_POWER;

	}
	else{

		motor[topRight] = 0;
		motor[topLeft] =  0;
		motor[bottomRight]= 0;
		motor[bottomLeft] = 0;
	}

}

