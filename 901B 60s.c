void clawGrab(long sec){

	motor[leftclaw] = FULL_POWER;
	motor[rightclaw] = FULL_POWER;
	wait1Msec(sec*1000);

}

void clawOpen(long sec){

	motor[leftclaw] = -FULL_POWER;
	motor[rightclaw] = -FULL_POWER;
	wait1Msec(sec*1000);

}

void SLift(){

	while(SensorValue[LiftP] < 250){
		motor[leftLiftMD] = LIFT_UP_VELOCITY;
		motor[leftLiftU] = LIFT_UP_VELOCITY;
		motor[rightLiftMD] = LIFT_UP_VELOCITY;
		motor[rightLiftU] = LIFT_UP_VELOCITY;
	}
	motor[leftLiftMD] = 0;
	motor[leftLiftU] = 0;
	motor[rightLiftMD] = 0;
	motor[rightLiftU] = 0;
}

void throw(bool move, long distance){
	if(move){

		long x = distance;

		SensorValue[TR] = 0;
		SensorValue[TL] = 0;

		while(SensorValue[TR]*cos(45)*2.54 <= calculate(x) && abs(SensorValue[TL])*cos(45)*2.54 <= calculate(x)&& SensorValue[LiftP] < 2500) {
			if(SensorValue[LiftP] < 2500){  //2520
				motor[leftLiftMD] = LIFT_UP_VELOCITY;
				motor[leftLiftU] = LIFT_UP_VELOCITY;
				motor[rightLiftMD] = LIFT_UP_VELOCITY;
				motor[rightLiftU] = LIFT_UP_VELOCITY;
				if(SensorValue[LiftP] > 2300 && SensorValue[LiftP] < 2400){
					clawOpen(0.2);
				}
			}
			else{
				motor[leftLiftMD] = 0;
				motor[leftLiftU] = 0;
				motor[rightLiftMD] = 0;
				motor[rightLiftU] = 0;
			}
			if(SensorValue[TR]*cos(45)*2.54 <= calculate(x) && abs(SensorValue[TL])*cos(45)*2.54 <= calculate(x)
				) {
				backwardstraight(calculate(x), true);
			}
		}
		motor[leftLiftMD] = 0;
		motor[leftLiftU] = 0;
		motor[rightLiftMD] = 0;
		motor[rightLiftU] = 0;
		motor[rightclaw] = 0;
		motor[leftclaw] = 0;

	}
	else if(move == false){

		SensorValue[TR] = 0;
		SensorValue[TL] = 0;

		while(SensorValue[LiftP] < 2500) {
			if(SensorValue[LiftP] < 2500){  //2520
				motor[leftLiftMD] = LIFT_UP_VELOCITY;
				motor[leftLiftU] = LIFT_UP_VELOCITY;
				motor[rightLiftMD] = LIFT_UP_VELOCITY;
				motor[rightLiftU] = LIFT_UP_VELOCITY;
				//if(SensorValue[LiftP] > 2100){
				//	clawOpen(0.2);
				//}
			}
			else{
				motor[leftLiftMD] = 0;
				motor[leftLiftU] = 0;
				motor[rightLiftMD] = 0;
				motor[rightLiftU] = 0;
			}
		}
		motor[leftLiftMD] = 0;
		motor[leftLiftU] = 0;
		motor[rightLiftMD] = 0;
		motor[rightLiftU] = 0;
		motor[rightclaw] = 0;
		motor[leftclaw] = 0;
	}

	/////////////////////////////////////////////////////////

}

